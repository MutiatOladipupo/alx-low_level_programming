#include <stdio.h>

/**
 * main - Prints the program name, followed by a new line.
 * @argc: The number of arguments supplied to the program.
 * @argv: An argument vector which is an array of pointers to the arguments that are passed to the program.
 *
 * Return: Return 0.
 */

int main(int argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
